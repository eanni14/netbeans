    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
nyetok.pack();
nyetok.setLocationRelativeTo(null);
nyetok.setVisible(true);

int row = table.getSelectedRow(); // dapatkan baris yang di-klik        
if (row != -1) { // cek apakah klik sekali
    JTable sourceTable = (JTable) evt.getSource(); // dapatkan tabel yang di-klik
    kodemenu.setText(sourceTable.getValueAt(row, 0).toString());
    namamenu.setText(sourceTable.getValueAt(row, 1).toString());
    kategori.setText(sourceTable.getValueAt(row, 2).toString());
    harga.setText(sourceTable.getValueAt(row, 3).toString());
    // Ambil nilai stok dari kolom ke-4 (indeks 3) dalam tabel
    Object stokValue = sourceTable.getValueAt(row, 4);
    if (stokValue != null) {
        stok.setText(stokValue.toString());
    } else {
        stok.setText("");
    }
    Object statusValue = sourceTable.getValueAt(row, 5);
    if (statusValue != null) {
        String status = statusValue.toString();
        // Periksa status dan atur JRadioButton sesuai dengan nilai yang ditemukan
        if (status.equalsIgnoreCase("Tersedia")) {
            tersedia.setSelected(true);
            tidaktersedia.setSelected(false);
        } else if (status.equalsIgnoreCase("Tidak Tersedia")) {
            tersedia.setSelected(false);
            tidaktersedia.setSelected(true);
        }
    }

    stok.requestFocus();

    // Ambil foto dari database
    try {
        String kodemenuValue = sourceTable.getValueAt(row, 0).toString();
        String sql = "SELECT Foto FROM menu_makanan WHERE Kd_menu='" + kodemenuValue + "'";
        Statement stat = koneksi.createStatement();
        ResultSet rs = stat.executeQuery(sql);
        
        if (rs.next()) {
            byte[] imageData = rs.getBytes("Foto");
            ImageIcon originalImageIcon = new ImageIcon(imageData);

            // Atur ukuran lebar dan tinggi foto
            int desiredWidth = 180; // Ukuran lebar yang diinginkan
            int desiredHeight = 180; // Ukuran tinggi yang diinginkan
            
            // Skala gambar ke ukuran yang diinginkan
            Image originalImage = originalImageIcon.getImage();
            Image scaledImage = originalImage.getScaledInstance(desiredWidth, desiredHeight, Image.SCALE_SMOOTH);
            
            // Buat ImageIcon dari gambar yang telah diskala
            ImageIcon scaledImageIcon = new ImageIcon(scaledImage);
            
            foto.setIcon(scaledImageIcon);
            
        }
        rs.close();
        stat.close();
        
    } catch (Exception exc) {
        System.err.println("Error: " + exc);
    }
    
}
    }//GEN-LAST:event_tableMouseClicked

