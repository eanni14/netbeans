        public void pencarian(String sql){
        Object[] Baris = {"Kode Menu","Nama Menu","Kategori","Harga","Stok","Ketersediaan"};
        tabmode = new DefaultTableModel(null, Baris);
        table.setModel(tabmode);
        try{
            java.sql.Statement stat = koneksi.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){              
                String kdm = hasil.getString("Kd_menu");
                String nmm = hasil.getString("Nm_menu");
                String ktg = hasil.getString("Kategori");
                String hrg = hasil.getString("Harga");
                String stk = hasil.getString("Stok");
                String sedia = hasil.getString("Stok_2");
                String[] data = {kdm,nmm,ktg,hrg,stk,sedia};
                tabmode.addRow(data);
            }
        } catch(Exception e){
        }
    }

